class RelationalOperators {
  public static void main(String[] args) {
    System.out.println("less than" + (30 < 90));
    System.out.println("less than" + (30 <= 30));
    System.out.println("greater than" + (30 > 50));
    System.out.println("greater than or equal to:" + (50 >= 50));
    System.out.println("equal to" + (30 == 90));
    System.out.println("not equal to" + (30 != 90));
  }
}