import java.util.*;
class Compare
  {
    public static void main(String[]args)
    {
      Scanner sc=new Scanner(System.in);
      int a,b;
      System.out.println("Enter integers");
      a=sc.nextInt();
      b=sc.nextInt();
      if(a==b)
      {
        System.out.println("Both values are equal");
      }
      if(a!=b)
      {
      {
        System.out.println(+a+"!="+b);
      }
      if(a<b)
      {
        System.out.println(+a+"<"+b);
      }
       if(a<=b)
      {
        System.out.println(+a+"<="+b);
      }
    }
    }
  }