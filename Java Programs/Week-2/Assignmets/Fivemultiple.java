import java.util.*;

class Fivemultiple {
  public static void main(String[] args) {
    Scanner s = new Scanner(System.in);
    System.out.println("Enter any number");
    int num = s.nextInt();
    if (num % 5 == 0) {
      System.out.println("You have enter a multiple of 5");
    } else {
      System.out.println("You entered is not a multiple of 5");
    }
    System.out.println("You entered is not a multiple of 5");
  }
}